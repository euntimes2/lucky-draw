// Parses the local participant spreadsheet into a static TS module.
// Usage: npm run build:participants
// Reads:  ./경품추첨용(최종★).xlsx (sheet "정리" if present, else first sheet)
// Writes: ./src/data/participants.ts

import { readSheet } from 'read-excel-file/node';
import { mkdirSync, writeFileSync } from 'node:fs';

const XLSX_PATH = './경품추첨용(최종★).xlsx';
const PREFERRED_SHEET = '정리';
const OUTPUT = './src/data/participants.ts';

const FIELD_ALIASES = {
  name: ['이름', '성명', '참가자', 'name', 'fullname', 'participant', 'candidate'],
  affiliation: [
    '학과', '전공', 'major', 'department',
    '단과대학', 'college', 'school', '학교',
    '소속', 'affiliation', 'organization', 'company', '회사', '기관', 'team',
  ],
  email: ['이메일', 'email', 'mail'],
  phone: ['휴대폰', '연락처', '전화', 'mobile', 'phone'],
};

function tokenizeHeader(value) {
  const normalized = String(value ?? '').normalize('NFKC').toLowerCase();
  return new Set(normalized.match(/[\p{L}\p{N}]+/gu) ?? []);
}

function findValue(indexed, aliases) {
  for (const alias of aliases) {
    const match = indexed.find(({ tokens }) => tokens.has(alias));
    if (match) return match.value;
  }
  return undefined;
}

function cleanText(value) {
  return String(value ?? '').normalize('NFKC').trim().replace(/\s+/g, ' ');
}

function cleanEmail(value) {
  return cleanText(value).toLowerCase();
}

function normalizeForKey(value) {
  return value.normalize('NFKC').trim().toLowerCase().replace(/\s+/g, ' ');
}

function hashId(input) {
  let h = 0;
  for (let i = 0; i < input.length; i += 1) {
    h = Math.imul(31, h) + input.charCodeAt(i);
  }
  return (h >>> 0).toString(36);
}

async function readPreferredSheet(path) {
  try {
    return await readSheet(path, PREFERRED_SHEET);
  } catch {
    return await readSheet(path);
  }
}

const rows = await readPreferredSheet(XLSX_PATH);
if (!rows.length) {
  throw new Error('Spreadsheet is empty.');
}

const headers = rows[0].map((v, i) => cleanText(v) || `column_${i + 1}`);
const records = rows.slice(1).map((row) => {
  const rec = {};
  headers.forEach((h, i) => {
    rec[h] = row[i] ?? '';
  });
  return rec;
});

const participants = [];
const seen = new Set();
let dropped = 0;
let duplicates = 0;

records.forEach((row, idx) => {
  const entries = Object.entries(row);
  const indexed = entries.map(([k, v]) => ({ tokens: tokenizeHeader(k), value: v }));
  const nameRaw = findValue(indexed, FIELD_ALIASES.name) ?? entries[0]?.[1];
  const name = cleanText(nameRaw);
  if (!name) {
    dropped += 1;
    return;
  }
  const affiliation = cleanText(findValue(indexed, FIELD_ALIASES.affiliation));
  const email = cleanEmail(findValue(indexed, FIELD_ALIASES.email));
  const phone = cleanText(findValue(indexed, FIELD_ALIASES.phone));
  const dedupeKey = email || `${normalizeForKey(name)}::${normalizeForKey(affiliation)}`;
  if (seen.has(dedupeKey)) {
    duplicates += 1;
    return;
  }
  seen.add(dedupeKey);
  const participant = { id: `seed-${idx + 1}-${hashId(dedupeKey)}`, name };
  if (affiliation) participant.affiliation = affiliation;
  if (email) participant.email = email;
  if (phone) participant.phone = phone;
  participants.push(participant);
});

mkdirSync('./src/data', { recursive: true });

const header = `// Auto-generated by scripts/build-participants.mjs — do not edit by hand.
// Regenerate with: npm run build:participants
/* eslint-disable */
import type { Participant } from '../types/participant';

export const DEFAULT_PARTICIPANTS: Participant[] = `;

writeFileSync(OUTPUT, header + JSON.stringify(participants, null, 2) + ';\n', 'utf8');

console.log(
  `Wrote ${participants.length} participants to ${OUTPUT} (dropped ${dropped}, duplicates ${duplicates})`
);
